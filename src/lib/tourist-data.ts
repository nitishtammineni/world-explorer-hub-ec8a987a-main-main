export type TouristPlace = {
  name: string;
  city?: string;
  blurb: string;
  query: string; // unsplash search query
};

export type TravelPackageTier = "budget" | "medium" | "luxury";

export type TravelPackage = {
  name: string;
  tier: TravelPackageTier;
  duration: string;
  price: number;
  inclusions: string[];
  highlights: string[];
  query: string;
  note?: string;
  isSpecial?: boolean;
};

export type CountryGuide = {
  hero: string; // hero query
  intro: string;
  places: TouristPlace[];
  packages?: TravelPackage[];
};

// Generate a consistent seed from any string for deterministic images
function stringToSeed(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash = hash & hash;
  }
  return Math.abs(hash);
}

// Generate a deterministic image URL using picsum.photos with a seed
// This ensures the same query always returns the same image
export function unsplash(query: string, w = 1200, h = 800) {
  const seed = stringToSeed(query);
  // Use picsum with seed for reliable, consistent images
  // Removed blur for high-quality, sharp travel photos
  return `https://picsum.photos/seed/${seed}/${w}/${h}`;
}

// Generate multiple gallery images for a place with different variations
export function getGalleryImages(query: string, count: number = 8) {
  const images = [];
  for (let i = 0; i < count; i++) {
    const seed = stringToSeed(`${query}-${i}`);
    images.push(`https://picsum.photos/seed/${seed}/800/600`);
  }
  return images;
}

