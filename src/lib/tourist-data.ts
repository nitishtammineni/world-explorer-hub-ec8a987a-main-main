export function getGuide(cca3: string): CountryGuide | null {
  // Handle special codes mapping
  const specialMappings: Record<string, string> = {
    SGM: "SGM", // Singapore & Malaysia Standard
    SGC: "SGC", // Singapore & Malaysia with Cruise
    AND: "AND", // Andaman
    LKD: "LKD", // Lakshadweep
    SCO: "SCO", // Scotland & London
    LKW: "LKW", // Lakshadweep Islands
    EU: "EU", // Europe Grand Tour
  };
  const mappedCode = specialMappings[cca3] || cca3;
  // First check GUIDES, then check SPECIAL_GUIDES
  return GUIDES[mappedCode] ?? SPECIAL_GUIDES[mappedCode] ?? null;
}
